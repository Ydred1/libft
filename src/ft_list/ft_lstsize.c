#include "../../libft.h"

int	ft_lstsize(t_list *lst)
{
	int		nb;
	t_list	*current;

	if (lst == NULL)
		return (0);
	nb = 1;
	current = lst;
	while (current->next)
	{
		current = current->next;
		nb++;
	}
	return (nb);
}
